package com.tcs.traf;

public class Non_Primitive_Data_Types {
	public static void main(String[] args) {
		
		String text = "Hello";
		String blank = " ";
		String name = "Shiv";
		System.out.println(text + blank + name);
		
		System.out.println("Hello" + " " + "Shiv");
		
		double myDouble = 6.8;
		System.out.println("My value of number is" +" " + myDouble);
		
		boolean abc = false;
		System.out.println(abc);
		
	}
}
