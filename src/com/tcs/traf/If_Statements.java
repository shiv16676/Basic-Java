package com.tcs.traf;

public class If_Statements {
	public static void main(String[] args) {

		int value = 5;

		if (value < 4) {
			System.out.println("true");
		} else if (value > 10) {
			System.out.println("false");
		} else if(value ==5){
			System.out.println("None of the above");
		}
		else{
			System.out.println("else");
		}
	}
}

